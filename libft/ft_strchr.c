#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	c &= 0xff;
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
